
// Оператор If

public class Lesson6_IF {
    public static void main(String[] args) {
        int MyInt = 15;
        if (MyInt < 10) {
            System.out.print("Переменная меньше");
        } else if (MyInt > 20) {
            System.out.print("Переменная больше");
        } else {
            System.out.print("Где-то посередине");
        }
    }
}
